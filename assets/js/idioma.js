function translateButtons(language) {
  const artStationButton = document.querySelector(".btn.btn-primary");
  const projectButtons = document.querySelectorAll(".custom-button");

  if (artStationButton) {
    if (language === "pt") {
      artStationButton.textContent = "Meu Perfil";
    } else {
      artStationButton.textContent = "My Profile";
    }
  }

  if (language === "pt") {
    projectButtons[0].textContent = "Visite o Projeto →";
    projectButtons[1].textContent = "Visite o Projeto →";
    projectButtons[2].textContent = "Visite o Projeto →";
    projectButtons[3].textContent = "Visite o Projeto →";
  } else {
    projectButtons[0].textContent = "Visit Project →";
    projectButtons[1].textContent = "Visit Project →";
    projectButtons[2].textContent = "Visit Project →";
    projectButtons[3].textContent = "Visit Project →";
  }
}

// Função para traduzir a seção de Contato
function translateContato(language) {
  const contatoHeader = document.querySelector(".contato h1");
  const contatoLink = document.querySelector(".contato a");

  if (language === "pt") {
    contatoHeader.textContent = "Podemos criar, mas criar a diferença";
    contatoLink.textContent = "Mande uma mensagem";
  } else {
    contatoHeader.textContent = "We can create, but create the difference";
    contatoLink.textContent = "Send a message";
  }
}

