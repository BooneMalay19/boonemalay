function translateArticles(language) {
  document.querySelector(".section-highlight2-title").textContent =
    translations[language].artigosTitulo;
  document.querySelector(".section-highlight2-description").innerHTML =
    translations[language].artigosDescricao;

  document.getElementById("main-news-category").textContent =
    translations[language].artigoPrincipalCategoria;
  document.getElementById("main-news-title").textContent =
    translations[language].artigoPrincipalTitulo;
  document.getElementById("main-news-link").textContent =
    translations[language].artigoPrincipalBotao;

  const newsItems = document.querySelectorAll(".news-item");
  newsItems[0].querySelector("h3").textContent =
    translations[language].artigoResponsividadeTitulo;
  for (let i = 1; i < newsItems.length; i++) {
    newsItems[i].querySelector(".btn-link").textContent =
      translations[language].artigoEmBreve;
  }
}

