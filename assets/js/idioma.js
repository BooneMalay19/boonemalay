// Funções de tradução
function translateMenu(language) {
  document.querySelector('.navbar-link[href="#"]').textContent =
    translations[language].home;
  document.querySelector('.navbar-link[href="#sobre"]').textContent =
    translations[language].sobre;
  document.querySelector('.navbar-link[href="#projetos"]').textContent =
    translations[language].projetos;
  document.querySelector('.navbar-link[href="#artes"]').textContent =
    translations[language].artes;
  document.querySelector('.navbar-link[href="#artigos"]').textContent =
    translations[language].artigos;
  document.querySelector('.navbar-link[href="#contatos"]').textContent =
    translations[language].contatos;
}

function translateAbout(language) {
  document.querySelector(".info h1").textContent =
    translations[language].sobreTitulo;
  document.querySelector(".subtitle").textContent =
    translations[language].sobreSubtitulo;
  document.querySelector(".description").textContent =
    translations[language].sobreDescricao;

  const socialStats = document.querySelectorAll(".social-stats div");
  socialStats[0].innerHTML = `<strong>+10 mil</strong> ${translations[language].seguidores} <a href="https://www.linkedin.com/in/BooneMalay">LinkedIn</a>`;
  socialStats[1].innerHTML = `<strong>+400</strong> ${translations[language].contribuicoes} <a href="https://github.com/BooneMalay19">GitHub</a>`;
  socialStats[2].innerHTML = `<strong>+20</strong> ${translations[language].repositorios} <a href="https://github.com/BooneMalay19?tab=repositories">Projetos</a>`;
}

